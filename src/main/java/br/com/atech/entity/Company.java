package br.com.atech.entity;

public class Company {
    private Long id;
    private String name;

    public Company() {
        super();
    }

    public Company(Long id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
